"use client"

import {
    Popover,
    PopoverContent,
    PopoverTrigger,
} from "@/components/ui/popover"
import { Button } from "@/components/ui/button"
import { Checkbox } from "@/components/ui/checkbox"
import { Label } from "@/components/ui/label"
import { ChevronDown } from "lucide-react"
import { updateUserProjects } from "@/app/actions/users"
import { useState, useTransition } from "react"
import { useToast } from "@/components/ui/use-toast"

type Project = {
    id: string
    name: string
}

export function ProjectSelect({
    userId,
    currentProjectIds,
    allProjects
}: {
    userId: string
    currentProjectIds: string[]
    allProjects: Project[]
}) {
    const [isPending, startTransition] = useTransition()
    const [open, setOpen] = useState(false)
    const [selectedProjectIds, setSelectedProjectIds] = useState<string[]>(currentProjectIds)
    const { toast } = useToast()

    function toggleProject(projectId: string) {
        const newIds = selectedProjectIds.includes(projectId)
            ? selectedProjectIds.filter(id => id !== projectId)
            : [...selectedProjectIds, projectId]
        setSelectedProjectIds(newIds)
    }

    function handleSave() {
        startTransition(async () => {
            const result = await updateUserProjects(userId, selectedProjectIds)

            if (result?.error) {
                setSelectedProjectIds(currentProjectIds) // Revert on error
                toast({
                    title: "Error",
                    description: result.error,
                    variant: "destructive"
                })
            } else {
                toast({
                    title: "Projects Updated",
                    description: `User is now assigned to ${selectedProjectIds.length} project(s)`,
                    variant: "success"
                })
                setOpen(false)
            }
        })
    }

    const selectedCount = selectedProjectIds.length

    // Get the name of the first selected project
    const firstProject = allProjects.find(p => selectedProjectIds.includes(p.id))

    const displayText = selectedCount === 0
        ? "No projects"
        : selectedCount === 1
            ? (firstProject?.name || "1 project")
            : `${firstProject?.name || "Project"} +${selectedCount - 1}`

    return (
        <Popover open={open} onOpenChange={setOpen}>
            <PopoverTrigger asChild>
                <Button
                    variant="outline"
                    className="w-[140px] justify-between"
                    disabled={isPending}
                >
                    <span className="truncate">{displayText}</span>
                    <ChevronDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
                </Button>
            </PopoverTrigger>
            <PopoverContent className="w-[250px] p-0" align="end">
                <div className="max-h-[300px] overflow-y-auto p-3 space-y-2">
                    {allProjects.map(project => (
                        <div
                            key={project.id}
                            className="flex items-center space-x-2 hover:bg-accent rounded p-2 cursor-pointer"
                            onClick={() => toggleProject(project.id)}
                        >
                            <Checkbox
                                checked={selectedProjectIds.includes(project.id)}
                                onCheckedChange={() => toggleProject(project.id)}
                            />
                            <Label className="text-sm font-normal cursor-pointer flex-1">
                                {project.name}
                            </Label>
                        </div>
                    ))}
                    {allProjects.length === 0 && (
                        <p className="text-sm text-muted-foreground text-center py-2">
                            No projects available
                        </p>
                    )}
                </div>
                <div className="border-t p-2">
                    <Button
                        onClick={handleSave}
                        className="w-full"
                        size="sm"
                        disabled={isPending}
                    >
                        {isPending ? 'Saving...' : 'Save'}
                    </Button>
                </div>
            </PopoverContent>
        </Popover>
    )
}

